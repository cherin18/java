public class If {
    public static void main(String[] args) {
        if (10 < 2) {
            System.out.println("True");
        }
        if (!(10 < 2)) {
            System.out.println("False");
        }

        if (10 > 2) {
            System.out.println("True");
        }
        if (!(10 > 2)) {
            System.out.println("False");
        }

        if (10 <= 2) {
            System.out.println("True");
        }
        if (!(10 <= 2)) {
            System.out.println("False");
        }

        if (10 >= 2) {
            System.out.println("True");
        }
        if (!(10 >= 2)) {
            System.out.println("False");
        }

        if (10 != 2) {
            System.out.println("True");
        }
        if (!(10 != 2)) {
            System.out.println("False");
        }

        if (10 == 2) {
            System.out.println("True");
        }
        if (!(10 == 2)) {
            System.out.println("False");
        }
    }
}
